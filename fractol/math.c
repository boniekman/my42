#include "fractol.h"

t_c			c_pow(t_c p)
{
	t_c		r;

	r.r = p.r * p.r - p.i * p.i;
	r.i = 2.0 * p.r * p.i;
	return (r);
}

t_c			c_add(t_c p, t_c c)
{
	t_c		r;

	r.r = p.r + c.r;
	r.i = p.i + c.i;
	return (r);
}
