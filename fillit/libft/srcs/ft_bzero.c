#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		lim;
	char	*copy;

	lim = (int)n;
	copy = (char *)s;
	while (lim > 0)
	{
		*copy = '\0';
		lim--;
		copy++;
	}
}
