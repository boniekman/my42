#include "fdf.h"

void		end(t_map *map)
{
	int		i;

	if (map == NULL)
		exit(0);
	i = 0;
	if (map->tab != NULL)
	{
		while (map->tab[i] && i < map->rows)
		{
			free(map->tab[i]);
			i++;
		}
		free(map->tab);
	}
	exit(0);
}
