#include "libft.h"

void	ft_putnbr(int n)
{
	int		nb;

	if (n == -2147483648)
	{
		ft_putstr("-2");
		n = 147483648;
	}
	nb = n;
	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -1;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
		ft_putchar(nb + '0');
}
