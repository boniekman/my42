#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	st;

	i = 0;
	st = ft_strlen(s1);
	while (s2[i] != '\0' && i < n)
	{
		s1[st + i] = s2[i];
		i++;
	}
	s1[st + i] = '\0';
	return (s1);
}
