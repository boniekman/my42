#include "libft.h"

void	ft_tabdel(void ***tab)
{
	int		i;
	char	**tmp;

	tmp = (char**)*tab;
	i = -1;
	while (tmp[++i])
		free(tmp[i]);
	free(tmp);
	*tab = NULL;
}
